public class TestIncrement {
    public static void main(String[] args) {
        int c;
        c = 5; // postincrement
        System.out.println( c );
        System.out.println( c++ );
        System.out.println( c );
        c = 5; // preincrement
        System.out.println( c );
        System.out.println( ++c );
        System.out.println( c );
    }
}